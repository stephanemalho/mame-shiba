import { readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDirectory, "..");
const publicDirectory = path.join(projectRoot, "public");
const outputFile = path.join(
  projectRoot,
  "lib",
  "generated-public-asset-paths.ts"
);

async function collectPublicFiles(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const nestedFiles = await Promise.all(
    entries.map(async (entry) => {
      const absolutePath = path.join(directory, entry.name);

      if (entry.isDirectory()) {
        return collectPublicFiles(absolutePath);
      }

      if (entry.isFile()) {
        return [absolutePath];
      }

      return [];
    })
  );

  return nestedFiles.flat().sort((left, right) => left.localeCompare(right));
}

function toPublicRoute(absolutePath) {
  const relativePath = path.relative(publicDirectory, absolutePath);
  return `/${relativePath.split(path.sep).join("/")}`;
}

const publicFiles = await collectPublicFiles(publicDirectory);
const publicRoutes = publicFiles.map(toPublicRoute);

const fileContents = `// This file is auto-generated by scripts/generate-public-asset-paths.mjs
// Do not edit it by hand.

export const publicAssetRoutes = ${JSON.stringify(publicRoutes, null, 4)} as const;
`;

await writeFile(outputFile, fileContents, "utf8");
